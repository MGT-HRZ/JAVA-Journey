public class markah2 {
    public static void main(String[] args)
    {
        int BahasaMelayu = 80;
        int BahasaInggeris = 90;
        int Matematik = 60;
        int Sejarah = 50;
        
        System.out.println("------------------------------");
        
        /*JUMLAH*/
        System.out.println("Jumlah markah =\t" +(BahasaMelayu + BahasaInggeris + Matematik + Sejarah));
        /*JUMLAH*/
        
        int Jumlah = BahasaMelayu + BahasaInggeris + Matematik + Sejarah;
        
        /*PURATA*/
        System.out.println("Purata markah =\t" +(Jumlah)/4);
        /*PURATA*/
        
        /*PERATUS*/
        System.out.println("Peratus markah =\t" +(Jumlah * 100)/400);
        /*PERATUS*/
        
        System.out.println("------------------------------");
    }   
}