package Loop_statement;

// Tajuk : Loop Statement

public class Loop_DoWhile1 {
    public static void main(String args[])
    {
        int x = 1;
        
        do {
            System.out.println(x);
            x++;
        }
        
        while (x <= 5);
        
        System.out.print("\n\n");
        
        int y = 5;
        
        do {
            System.out.println(y);
            y += 5;
        }
        
        while (y <= 25);
        
    }
}
