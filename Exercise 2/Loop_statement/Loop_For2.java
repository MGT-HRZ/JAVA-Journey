public class Loop_For2 {
    public static void main(String args[])
    {
        for (int i = 1; i < 20; i++){
            System.out.print(i + ", ");
        }
        
        System.out.print("\n\n");
        
        for (int a = 1; a <= 3; a++){
            System.out.print("success always, ");
        }
        
        
    }
}
